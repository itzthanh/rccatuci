			<h1>About Us</h1>
		</div>
		<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The <span style="color:red">RED CROSS CLUB</span> here at UC Irvine is an active service club on campus that is made up of a group of students united by a common cause&#58; to comfort, care, and improve the lives of people all over the world as well as the environment. The guiding principles that are at the heart of the Red Cross Club are community service, volunteering, donating, giving back, lending a helping hand, and fundraising. We believe that such a positive difference is not only made in our community, but communites around the globe.<br><br>

		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The club has maintained itself as an active service club that is undeterred by obstacles and determined to make whatever difference can be made in this world. This is accomplished through the strength and teamwork of our members, and our dedication to service that helps our community in whatever way we are able. We are one of the largest on campus volunteer organizations and have consistently made an effort to give back more and more each year going forward. The Red Cross Club thrives on the concepts of unity and volunteering and combines these two to create a beautiful merging of students and community members working together towards a greater good.<br><br>

		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;RCC has been fortunate enough to be able to be actively involved in volunteering opportunities throughout Orange County. We have attended and assisted at multiple walks that support an end to cancer, AIDS, multiple sclerosis and Alzheimer’s in the previous quarter alone. Through our Working Wardrobes events we aim to help empower men, women, veterans and young adults overcoming difficult challenges to confidently enter the workforce and achieve self-sufficiency. We  organize clothes for potential clients searching for business attire. Clients range from homeless individuals, veterans, and anyone else looking to get back on their feet. We have also helped on events targeted for children, like the annual Spooktakular Days Halloween carnival or the MESA science fair for children in middle and high school. Red Cross has been lucky enough to even get to help out at children hospitals in the area, such as the Healthbridge Children’s Hospital which we are grateful to say we frequent. Besides helping people, Red Cross also strives to beautify our community. Members have been able to create ecologically friendly area for native plant species to thrive through our Back to Natives events we regularly attend and clean up our local beaches during our OC Coastkeeper events. We are involved in so much more so come check us out!<br><br>

