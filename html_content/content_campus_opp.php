			<span style="text-transform:uppercase;text-decoration:underline">PH 91: DISPARITIES IN MEDICINE FOR WINTER 2017</span><br><br>
			Are you interested in medicine? Would you like to explore the different fields of health? Want to learn more about patient and physician interactions?<br><br>
	 
			Sign up for <span style="text-decoration:underline">PH 91</span>: DISPARITIES IN MEDICINE FOR WINTER 2017 !<br><br>
			 
			Disparities in Medicine (Public Health 91) is a winter course organized  by UCI’s School of Medicine, Public Health, and Office of Equal Opportunity and Diversity that aims to provide information that will assist healthcare professionals in providing culturally competent care to a diverse society and stimulate interest in cross-cultural medicine among students. We strive to address stigmas and cultural diversity amongst various medical topics, such as Women’s Health, Refugee Health, LGBTQ Health, and many others in order to spread awareness.<br><br>
			 
			This is a great course for students who are interested in the medical field. This class provides a unique educational experience and goes beyond treating the symptoms to encourage treating the patient. We encourage everyone to enroll, even if you have the slightest interest in medicine. If you cannot enroll, we still welcome you to stop by to sit in any lecture that you may be interested in.  For more information, please see the <a href="../PH91Syllabus.pdf">syllabus</a> and <a href="../PH91Flyer.pdf">flyer</a>.<br><br>
			 
			This is a Pass/No Pass 2 unit course held on Wednesdays, 5PM - 6:50PM located in HG 1800.<br><br>
			 
			Enrollment takes place via Webreg with course code <span style="color:red">89140</span> and is open to all majors, we encourage diversity!<br><br>
			<span class="lineSeparator"></span><br>
			<span style="text-transform:uppercase;text-decoration:underline">The Health Scholar program at Hoag Memorial Hospital Presbyterian</span><br><br>
			Are you interested in pursuing a career in healthcare? The Health Scholar program at Hoag Memorial Hospital Presbyterian in Newport Beach is currently accepting applications for its rotation. This program allows students to directly interact with both the patients and the staff and gain invaluable clinical experience at a renowned hospital. In addition to providing patient care, students are given the opportunity to network with hospital staff, attend professional lectures, and improve their leadership skills. Health Scholars are able to rotate between twenty different departments, which range from Emergency Department, Labor and Delivery, Oncology, Interventional Radiology, and many more!<br><br>
			 
			The 2017A rotation is now open to applications and the application deadline is <span style="color:red">11/16/15</span>.<br><br>
			If you are interested in applying, click<span style="text-align:center"> <a href="https://hcti.copehealthsolutions.com/default.aspx">here</a></span>.<br><br>
			For more information on the program requirements, admission process, and general FAQ, click<span style="text-align:center"> <a href="http://copehealthscholars.org/prospective-applicants/cce-application">here</a></span>.<br><br>
			 
