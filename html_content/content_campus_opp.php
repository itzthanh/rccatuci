		CORE (Campus Organizations Resources &amp; Education)
		Sponsored by the Office of Campus organizations, CORE aims to help student involvement via outreach, education and support. They provide multiple workshops throughout the year on campus org fundamentals of how to run a meeting, to applying for campus funding. CORE has been generous enough to contribute as one of the sponsors for Red Cross’ Thanksgiving Dinner event for Shelter families and our Charity Gala event to raise funds for Corazon de Vida. As a club we try to attend many of their workshops and leadership events through the year!<br><br>

		<span style="text-align:center">Website&#58; <a href="http://campusorgs.uci.edu/about/CORE.php">http://campusorgs.uci.edu/about/CORE.php</a></span><br><br>

		Cross Cultural Center
		The CCC at UCI aims to create and maintain a socially just campus and foster the cultural identities within the UCI community as well as providing opportunities to students to develop leadership and engage with the community. The CCC has been a sponsor of Red Cross’ Thanksgiving Dinner events and offer many workshops and volunteer opportunities.<br><br>

		<span style="text-align:center">Website&#58; <a href="https://www.ccc.uci.edu/">https://www.ccc.uci.edu/</a></span><br><br>

		SOAR (Student Outreach and Retention) Center
		SOAR aims to provide outreach and opportunities to the students of UCI. They work to increase student engagement on and off campus and create a supportive social environment. They have a variety of retention projects they are always involved in, like the SOAR Food Pantry for UCI students who need access to emergency food. They have a variety of events throughout the year as well.<br><br>
		
